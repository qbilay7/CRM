    public ResponseEntity<String> register(ClubRegistrationRequest registrationRequest) {
            if (registrationRequest.getCompanyName() == null || registrationRequest.getCompanyNumber() == null || registrationRequest.getCompanyEmail() == null || registrationRequest.getPassword() == null) {
               return ResponseEntity.status(403).body("Do not submit null properties.");
            }
            if(registrationRequest.getCompanyName().length() > 35 || registrationRequest.getCompanyLocation().length() > 45){
                return ResponseEntity.status(403).body("Name or location is too long.");
            }
            boolean isValid = emailValidator.test(registrationRequest.getCompanyEmail());
            if (!isValid) {
               return ResponseEntity.status(403).body("Please enter a valid email.");
            }
            if (!emailValidator.isValidPhoneNumber(registrationRequest.getCompanyNumber())) {
                return ResponseEntity.status(403).body("Please enter the phone number in valid format.");
            }

            try {
                String token = companyService.signUpCompany(
                        new Company(
                                registrationRequest.getCompanyName(),
                                registrationRequest.getCompanyEmail(),
                                registrationRequest.getCompanyNumber(),
                                registrationRequest.getCompanyLocation(),
                                registrationRequest.getPassword()
                        )
                );
                //String link= "http://localhost:8080/golfsprint/api/club/confirm?token="+token;
                //emailService.send(registrationRequest.getCompanyEmail(),buildEmail(registrationRequest.getCompanyName(),link));

            }catch (IllegalStateException e){
                return ResponseEntity.status(403).body("An account with that email already exists.");
            }
        return ResponseEntity.ok("Success.");
    }

